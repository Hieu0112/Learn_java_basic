    private void loadData(){
        File f=new File(fqlMuon);
        if(f.exists())
            lqlMuon=IOEFIle.Doc(fqlMuon);
        else
            lqlMuon=new ArrayList<>();
        
        File fs=new File(fsach);
        if(fs.exists())
            lsach=IOEFIle.Doc(fsach);
        else
            lsach=new ArrayList<>();
    }
    private void load2Ma(){
        cbMabd.removeAllItems();
        cbMaSach.removeAllItems();
        List<BanDoc>l1=IOEFIle.Doc(fbandoc);
        if(l1!=null){
        for(BanDoc i:l1){
            cbMabd.addItem(i.getMa()+"");
            }
        }
        lsach=IOEFIle.Doc(fsach);
        for(Sach i:lsach){
            cbMaSach.addItem(i.getMa()+"");
            }
    }
    private boolean trung2Ma(int mabd,int mas){
        for(QuanLyMuonSach i:lqlMuon){
            if(i.getBandoc().getMa()==mabd && i.getSach().getMa()==mas)
                return true;
        }
        return false;
    }
    private int demDauSach(int mabd){
        int count=0;
        for (QuanLyMuonSach quanLyMuonSach : lqlMuon) {
            if(quanLyMuonSach.getBandoc().getMa()==mabd)
                count++;
        }
        return count;
    }
