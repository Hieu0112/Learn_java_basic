    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int mabd=Integer.parseInt(cbMabd.getSelectedItem().toString());
        int masach=Integer.parseInt(cbMaSach.getSelectedItem().toString());
        if(trung2Ma(mabd, masach)){
            JOptionPane.showMessageDialog(this, "2 ma bi trung");
            cbMabd.requestFocus();
            return;
        }
        int sl=0;
        int sltv=getSachByMa(masach).getSl();
        
        try{
            sl=Integer.parseInt(jTextField1.getText());
            if(sl>sltv){
                JOptionPane.showMessageDialog(this, "Nhap So");
                jTextField1.requestFocus();
                return;
            }
            String tt=jTextField2.getText();
            QuanLyMuonSach ql=new QuanLyMuonSach(getBanDocByMa(mabd), getSachByMa(masach), sl, tt);
            tm.addRow(ql.toObject());
            lqlMuon.add(ql);
            getSachByMa(masach).setSl(sltv-sl);
            IOEFIle.Viet(fsach, lsach);
        
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Nhap So");
            jTextField1.requestFocus();
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

